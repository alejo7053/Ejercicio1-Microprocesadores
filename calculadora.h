/**
* \ brief funcion que resta
* \param parametro1 operando ingresado
* \param parametro2 operando ingresado
* \return retorna  el resultado de la resta
*/
int resta(int parametro1, int parametro2);

/**
* \ brief funcion que hace multiplicacion
* \param parametro1 operando ingresado
* \param parametro2 operando ingresado
* \return retorna  el resultado de la multiplicacion
*/
int multiplacion(int parametro1, int parametro2);

/**
* \ brief funcion hace division
* \param parametro1 operando ingresado
* \param parametro2 operando ingresado
* \return retorna  el resultado de la division
*/
int division(int parametro1, int parametro2);
